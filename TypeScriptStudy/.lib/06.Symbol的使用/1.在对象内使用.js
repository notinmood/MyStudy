"use strict";
// +--------------------------------------------------------------------------
// |::说明·| symbol 类型的属性名称，一定要用[]定义和调用，[]内的名称不能有引号（否则就是普通字符串类型的属性名称了）。
// +--------------------------------------------------------------------------
var _c;
var sym = Symbol();
var obj = (_c = {},
    _c[sym] = "value",
    _c.xx = "abc",
    _c);
console.log(obj[sym]); // "value"
console.log(obj["xx"]); // "abc"
